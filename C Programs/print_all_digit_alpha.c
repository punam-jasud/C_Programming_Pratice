#include<stdio.h>

void main()
{
	int i;

	printf("\n\nAlphabets in Upper Case :\n\n");
	for(i=65;i<=90;i++)
		printf("%c ",i);


	printf("\n\n");

	printf("\n\nAlphabets in Lower Case :\n\n");
	for(i=97;i<=122;i++)
		printf("%c ",i);

	printf("\n\n");

	printf("\nDigits :\n\n");
	for(i=48;i<=57;i++)
		printf("%c ",i);

	printf("\n\n");


}
